<?php


/** Magic Methods:
 * special methods in a class.
 * override the default actions when the object performs the actions.
 * the names of magic methods start with a double underscore (__).
 *
 *
 * Examples:
 * __construct(): invoked automatically when the object is created.
 * __destruct(): invoked automatically when the object is deleted.
 *
 *
 * __get(): invoked when (reading) the value from inaccessible (property).
 * __set(): invoked when (writing) a value to inaccessible (property).
 *
 *
 * __call(): triggered when invoking an inaccessible instance method.
 * __callStatic(): triggered when invoking an inaccessible static method.
 *
 *
 * __clone(): put your conditions while cloning an object into another.
 *
 *
 * __toString(): invoked when an object of a class is treated as a string.
 *
 * __invoke(): is invoked when an object is called as a function
 *
 *
 * // the following are covered in serialization tutorial:
 * __sleep()
 * __wakeup()
 *
 * __serialize()
 * __unserialize()
 *
 * __isset()
 * __unset()
 *
 * __set_state()
 *
 * __debugInfo()
 */
