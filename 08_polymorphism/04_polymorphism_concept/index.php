// Polymorphism:
// one thing can have many forms
// e.g. SMS object can be Vodafone, We, Etisalate, Orange...
// all of them share the same functionalities, but each one implement it in it's own way
// you can replace any one with other without change you core code
// By using polymorphism, you can reduce coupling and increase code reusability.

// Polymorphism helps you create a generic framework that takes the different object types that share the same interface.

// Polymorphism forms:
// Interfaces
// abstract classes
// method overloading
// method overriding
// operator overriding
